package com.zhangyi.reboundrecyclelayout.bean;

/**
 * Created by zhangyi on 2016/12/8.
 */

public class ProductionBean  {

    /**
     * 作品背景图
     */
    private String image;
    /**
     * 作品名称
     */
    private String name;
    /**
     * 作演播
     */
    private String anchor;
    /**
     * 播放量
     */
    private String playNumber;
    /**
     * 订阅量
     */
    private String subscribeNumber;
    /**
     * 作品简介
     */
    private String introduction;
    /**
     * 集数
     */
    private String chapterNumber;


    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAnchor() {
        return anchor;
    }

    public void setAnchor(String anchor) {
        this.anchor = anchor;
    }

    public String getPlayNumber() {
        return playNumber;
    }

    public void setPlayNumber(String playNumber) {
        this.playNumber = playNumber;
    }

    public String getSubscribeNumber() {
        return subscribeNumber;
    }

    public void setSubscribeNumber(String subscribeNumber) {
        this.subscribeNumber = subscribeNumber;
    }

    public String getIntroduction() {
        return introduction;
    }

    public void setIntroduction(String introduction) {
        this.introduction = introduction;
    }

    public String getChapterNumber() {
        return chapterNumber;
    }

    public void setChapterNumber(String chapterNumber) {
        this.chapterNumber = chapterNumber;
    }

}
